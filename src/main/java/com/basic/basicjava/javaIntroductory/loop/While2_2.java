package com.basic.basicjava.javaIntroductory.loop;

public class While2_2 {
  public static void main(String[] args) {
    int sum = 0;
    
    int i = 1;
    
    sum += i;
    System.out.println("i " + i + " sum = " + sum);
    i++;
    
    sum += 2;
    System.out.println("i " + i + " sum = " + sum);
    i++;
    
    sum += 3;
    System.out.println("i " + i + " sum = " + sum);
  }
}
