package com.basic.basicjava.javaIntroductory.variable;

public class Var7 {
  public static void main(String[] args) {
    int a = 100;
    double b = 10.5;
    boolean c = true;
    char d = 'D';
    String e = "Hello";
    
    /*
      100, 10.5, true, D, Hello  이런 개발자가 직접 입력한 고정된 값 --> 리터럴(literal) 이라고 함
     */
    
    System.out.println("a = 정수 : " + a);
    System.out.println("b = 실수 : " + b);
    System.out.println("c = 참/거짓 : " + c);
    System.out.println("d = 문자 : " + d);
    System.out.println("e = 문자열 : " + e);
  }
}
